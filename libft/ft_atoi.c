#include "libft.h"

int	ft_atoi(char *str)
{
	int			i;
	long int	k;
	int			fu;

	i = 0;
	k = 0;
	fu = 0;
	while (str[i] == '\n' || str[i] == '\r' || str[i] == '\v' ||
	str[i] == ' ' || str[i] == '\t' || str[i] == '\f')
		i++;
	fu = ((str[i] == '-') ? -1 : 1);
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		k = (k * 10) + (str[i] - 48);
		if ((k > 2147483647 && fu == 1) ||
			(k < -2147483648 && fu == -1))
			return ((fu == 1) ? -1 : 0);
		i++;
	}
	return (k * fu);
}
